1. Basic => [
    1. Class & Object
    2. Property & Method
    3. Constructor
    4. Object Type
    5. Inheritance
    6. Visibility / Access Modifier
    7. Setter & Getter
    8. Static Method
]

2. Advanced => [
    a. Abstract & Interface
    b. Interceptor
    c. Object Cloning
    d. Callbacks & Closures
    e. Namespaces & Autoloading
    f. etc.
]

*/
