<?php 
// Apa itu OOP?

/*
Adalah gaya pemrograman, programming style, atau paradigma pemrograman. 

Karakteristik:
a. Menyusun sebuah kode program dan struktur data ke dalam objek
b. Objek adalah unit dasar dari program
c. Di dalam objek, kita dapat menyimpan data(property) dan perilaku(method)
d. Objek bisa saling berinteraksi 

Kelebihan:
a. Dengan OOP, kita lebih mudah merepresentasikan dunia nyata.
b. Enkapsulasi => memungkinkan program kita menjadi aman
   Abstraksi Data => mampu menyembunyikan kerumitan program kita
c. Reusability
d. Skalabilitas & Ekstensibilitas akan memudahkan programmer apabila programnya menjadi lebih besar
e. Kemudahan pengelolaan
f. Kolaborasi
g. Digunakan oleh framework

Di dalam paradigma pemrograman, ada 2 jenis seperti:
1. Procedural Programming
2. OOP
*/

// Apa itu Procedural Programing?

/*
Karakteristik:
a. Instruksi dilakukan langkah demi langkah
b. Kita berusaha memecah program menjadi bagian-bagian kecil (kita membuat file fungsi, index, dll)
c. Disebut prosedur, subroutine atau function
d. Dibaca secara linear / Top to Buttom

Kelebihan:
a. To the point
b. Simple untuk compiler & interpreter
c. Mudah ditelusuri
d. Membutuhkan lebih sedikit memory daripada OOP
*/

// Procedural vs OOP

/*
Procedural = jika ingin dikerjakan sendiri dan programnya simple
OOP = jika dikerjakan secara kolaborasi dan programnya besar
*/

// KONSEP OOP pada PHP

